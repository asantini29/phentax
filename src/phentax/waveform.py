class IMRPhenomTHM:
    """
    Class for generating multi-polar, aligned-spin IMRPhenomTHM waveforms.

    Parameters
    ----------
    higher_modes : Optional[Array | list | str], default "all"
        Higher modes to include beyond (2,2). Can be:
        - None: only (2,2) mode.
        - "all": include all allowed higher modes.
        - list or Array of integers: specific modes to include (e.g., [21, 33]). Only the positive m modes have to be specified.
    include_negative_modes : bool, default True
        Whether to include negative m modes by symmetry.
    coarse_grain : bool, default False
        Whether to use adaptive coarse-graining for time grid generation.
    use_splines : bool, default False
        Whether to use cubic spline interpolation for output waveforms.
    t_low_fit : bool, default True
        Whether to use the default fit for t_low in t(f).
    atol : float, default 1e-12
        Absolute tolerance for the t(f) root finding.
    rtol : float, default 1e-12
        Relative tolerance for the t(f) root finding.
    """

    def __init__(
        self,
        higher_modes: Optional[Array | list | str] = "all",
        include_negative_modes: bool = True,
        coarse_grain: bool = False,
        use_splines: bool = False,
        t_low_fit: bool = True,  # Use default fit for t_low if True.
        atol: float = 1e-12,
        rtol: float = 1e-12,
        # todo add time options. return interpolant / dense array / sparse array
    ):
        """
        Initialize the IMRPhenomTHM waveform generator.
        """

        if higher_modes is None:
            self.higher_modes = jnp.array([])
            self.has_hm = False
            logger.debug("No higher modes will be included.")

            self.ells: Array = jnp.array([2], dtype=jnp.int32)
            self.mms: Array = jnp.array([2], dtype=jnp.int32)

        else:
            if isinstance(higher_modes, str) and higher_modes == "all":
                self.higher_modes = jnp.asarray(ALLOWED_POSITIVE_HMS)

            elif isinstance(higher_modes, (list, Array)):
                self.higher_modes = jnp.asarray(higher_modes)
            else:
                raise TypeError(
                    "higher_modes must be None, a list, an array, or 'all'."
                )
            logger.debug("Including higher modes: %s", self.higher_modes)
            self.has_hm = True
            self.odd_modes_mask = (self.higher_modes % 2 == 1)[:, None]

            # extract ells and mms
            self.ells, self.mms = mode_to_lm(self.higher_modes)
            self.ells = jnp.concatenate([jnp.array([2]), self.ells])
            self.mms = jnp.concatenate([jnp.array([2]), self.mms])

        self.include_negative_modes = include_negative_modes

        if self.include_negative_modes:
            logger.debug("Negative m modes will be generated by symmetry.")
            # add negative modes to ells and mms
            self.negative_ls = self.ells[self.mms != 0]
            self.negative_mms = -self.mms[self.mms != 0]

        else:
            logger.debug("Negative m modes will NOT be included.")
            self.negative_ls = jnp.array([])
            self.negative_mms = jnp.array([])

        self.coarse_grain = coarse_grain
        logger.debug("Coarse graining set to %s", self.coarse_grain)

        self.use_splines = use_splines
        logger.debug("Using splines set to %s", self.use_splines)

        if t_low_fit:
            logger.debug("Using fit in t(f): t_low = - 0.015 * f^(-2.7)")
            self.t_low = 0.0  # will be set in get_time_of_frequency
        else:
            logger.debug("Using default t_low = -1e9 M in t(f).")
            self.t_low = -1e9

        self.atol = atol
        self.rtol = rtol

    @jax.jit(static_argnames="self")
    def _compute_coeffs_22(
        self,
        wf_params: WaveformParams,
    ) -> tuple[WaveformParams, AmplitudeCoeffs, PhaseCoeffs]:
        """
        Compute amplitude and phase coefficients for the (2,2) mode.
        This updates relevant derived parameters in wf_params, and sets the time array if not provided.

        Parameters
        ----------
        wf_params : WaveformParams
            Waveform parameters of the binary.
        Returns
        -------
        wf_params : WaveformParams
            Updated derived parameters.
        amplitude_coeffs : AmplitudeCoeffs
            Amplitude coefficients for the (2,2) mode.
        phase_coeffs : PhaseCoeffs
            Phase coefficients for the (2,2) mode.
        """
        wf_params, phase_coeffs = compute_phase_coeffs_22(wf_params)
        amplitude_coeffs = compute_amplitude_coeffs_22(wf_params, phase_coeffs)

        return wf_params, amplitude_coeffs, phase_coeffs

    @jax.jit(static_argnames="self")
    def _compute_amp_phase_22(
        self,
        times: Array,
        mask: Array,
        wf_params: WaveformParams,
        amplitude_coeffs: AmplitudeCoeffs,
        phase_coeffs: PhaseCoeffs,
    ) -> tuple[Array, Array]:
        """
        Compute amplitude and phase for the (2,2) mode.

        Parameters
        ----------
        times : Array
            Time array.
        mask : Array
            Boolean mask indicating valid time points.
        wf_params : WaveformParams
            Waveform parameters of the binary.
        amplitude_coeffs : AmplitudeCoeffs
            Amplitude coefficients for the (2,2) mode.
        phase_coeffs : PhaseCoeffs
            Phase coefficients for the (2,2) mode.

        Returns
        -------
        amplitude_22 : Array
            Amplitude array for the (2,2) mode.
        phase_22 : Array
            Phase array for the (2,2) mode.
        """

        amplitude_22 = jnp.abs(
            masked_evaluate(
                times,
                mask,
                lambda t: imr_amplitude(
                    t,
                    wf_params.eta,
                    amplitude_coeffs,
                    phase_coeffs,
                ),
            )
        )

        phase_22 = masked_evaluate(
            times,
            mask,
            lambda t: imr_phase(
                t,
                wf_params.eta,
                phase_coeffs,
            )
            - phase_coeffs.phoff
            - phase_coeffs.phiref0,
            fill_value=0.0,
        )

        return amplitude_22[None, :], phase_22[None, :]
    
